for each row execute function public.set_updated_at();

create trigger set_products_updated_at
before update on public.products
for each row execute function public.set_updated_at();

create trigger set_orders_updated_at
before update on public.orders
for each row execute function public.set_updated_at();

create or replace function public.normalize_username(raw_username text)
returns text
language sql
immutable
as $$
  select left(
    regexp_replace(lower(coalesce(raw_username, 'runner')), '[^a-z0-9_]', '', 'g'),
    32
  );
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  base_username text;
  final_username text;
  suffix text;
begin
  base_username := public.normalize_username(
    coalesce(
      new.raw_user_meta_data->>'username',
      split_part(new.email, '@', 1),
      'runner'
    )
  );

  if length(base_username) < 3 then
    base_username := base_username || 'run';
  end if;

  final_username := left(base_username, 32);

  while exists(select 1 from public.profiles where username = final_username) loop
    suffix := '_' || substr(gen_random_uuid()::text, 1, 6);
    final_username := left(base_username, 32 - length(suffix)) || suffix;
  end loop;

  insert into public.profiles (
    id,
    email,
    username,
    display_name,
    avatar_url
  )
  values (
    new.id,
    coalesce(new.email, ''),
    final_username,
    coalesce(new.raw_user_meta_data->>'display_name', new.raw_user_meta_data->>'full_name'),
    new.raw_user_meta_data->>'avatar_url'
  );

  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.get_email_for_username(lookup_username text)
returns text
language sql
stable
security definer
set search_path = public
as $$
  select email
  from public.profiles
  where username = public.normalize_username(lookup_username)
  limit 1;
$$;

