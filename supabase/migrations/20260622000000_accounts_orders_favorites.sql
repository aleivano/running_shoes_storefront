create policy "profiles are readable by owner"
on public.profiles for select
using (auth.uid() = id);

create policy "profiles are insertable by owner"
on public.profiles for insert
with check (auth.uid() = id);

create policy "profiles are updatable by owner"
on public.profiles for update
using (auth.uid() = id)
with check (auth.uid() = id);

create policy "active products are public"
on public.products for select
using (status = 'active');

create policy "favorites are readable by owner"
on public.favorites for select
using (auth.uid() = user_id);

create policy "favorites are insertable by owner"
on public.favorites for insert
with check (auth.uid() = user_id);

create policy "favorites are deletable by owner"
on public.favorites for delete
using (auth.uid() = user_id);

create policy "orders are readable by owner"
on public.orders for select
using (auth.uid() = user_id);

create policy "orders are insertable by owner"
on public.orders for insert
with check (auth.uid() = user_id);

create policy "pending orders are cancelable by owner"
on public.orders for update
using (auth.uid() = user_id and status in ('pending', 'processing'))
with check (auth.uid() = user_id and status = 'canceled');

create policy "order items are readable by order owner"
on public.order_items for select
using (
  exists (
    select 1 from public.orders
    where orders.id = order_items.order_id
      and orders.user_id = auth.uid()
  )
);

create policy "order items are insertable by order owner"
on public.order_items for insert
with check (
  exists (
    select 1 from public.orders
    where orders.id = order_items.order_id
      and orders.user_id = auth.uid()
  )
);

create policy "order events are readable by order owner"
